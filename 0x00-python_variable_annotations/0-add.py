#!/usr/bin/env python3
"""
Module that provides a function to add two floats.
"""


def add(a: float, b: float) -> float:
    """
    Add two float numbers and return their sum.
    
    Args:
        a (float): First float number
        b (float): Second float number
    
    Returns:
        float: The sum of a and b
    """
    return a + b
